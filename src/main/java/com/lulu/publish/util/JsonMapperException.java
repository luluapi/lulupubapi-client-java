package com.lulu.publish.util;

public class JsonMapperException extends Exception {

    public JsonMapperException() {
        super();
    }

    public JsonMapperException(String s) {
        super(s);
    }

    public JsonMapperException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public JsonMapperException(Throwable throwable) {
        super(throwable);
    }
}
